           <h1> Quem Somos </h1>

           <p>Homer é uma plataforma digital inovadora que traz um ambiente seguro e dinâmico para a  busca de mão de obra qualificada em geral.</p>

           <p>Ele foi projetado em  2016 pela empresa CRRAV e desenvolvida no mesmo ano pela empresa Skynerd.</p>

           <p>O Objetivo do Homer é facilitar a vida dos nossos usuários, conectando quem precisa de uma força em casa a prestadores de serviço competentes de forma ágil e eficiente.</p>

           <h1> Nossa Missão </h1>

            <p>Facilitar a vida dos nossos usuários promovendo o encontro entre contratantes e prestadores de serviço em um só lugar, de forma ágil e eficiente.</p>

           <h1> Visão </h1>
           <p>Evoluir a forma de contratações de serviços para qualquer lugar, e ser referência no mercado como a melhor opção para os usuários, colaboradores e investidores, pelo relacionamento diferenciado que oferecemos.</p>

